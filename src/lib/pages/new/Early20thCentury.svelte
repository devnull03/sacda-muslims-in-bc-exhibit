        In addition to the stories of early Muslim migrants, it is important to
        note the reception they received within Canada. In the 1920s the idea of
        the Ottoman empire as the enemy in the context of WW1 was conflated with
        all Muslims. Scholars who have studied Muslims across the world have
        reminded us that this idea of Muslims as being dangerous and “alien” was
        by no means restricted to one particular area of the centres of Empire
        or what today we refer to as Western democracies. In the 1920s, Muslims
        were portrayed in Canadian newspapers as “fanatics”, the stories told
        about South Asian Muslims encouraged an exotic conception of the Muslim
        man who was portrayed as specifically being out to “make Christian
        America Mohammedan”.

        <br /><br />
        However, this task was made much more confusing for researchers by the fact
        that some people would take on pseudonyms when they came to Canada.
        <br /><br />
        Such was the case of Husain Rahim, whose real name was Chagan Khairaj Varma.
        Husain Rahim was helping South Asians workers in British Columbia and was
        also a supporter of the anti-colonial movement and party of the time, the
        Ghadr party. As a result, changing his name was a way to keep him safe from
        the colonial police. This did not mean that his change of name kept him safe.
        In fact, Varma (or Rahim if you prefer) was threatened with deportation in
        the 1910s, when Canadian immigration laws were restrictive and explicitly
        racist. However, the court was surprised to learn that Rahim was actually
        a citizen of Honoluulu and therefore, could not be arrested! Further information
        on Rahim can be found under the section on
