    Canada and South Asian Canadian Muslims after 9/11
  </h1>
  <div class="desc-text">
    The attacks on the twin towers on the 11th of September 2011, and America’s
    response to them, forever changed the way that Muslims were perceived
    globally. Scholars like Edward Said have pointed out that Muslims came to be
    seen as a “flattened” category where the diversity of cultural and religious
    practices was erased, particularly in the US. Canada’s proximity to the US
    meant that it was seen as central to the response to 9/11 even being
    specifically mentioned in the US Patriot Act under the section “Protecting
    the Northern Border.” Scholars have argued that Canada’s response to 9/11
    was affected by the need to appease the US so as to keep people and good
    flowing across the border. In 2001However, the repercussions of these
    policies was an increase in racial profiling, particularly of Muslim men,
    who in some very high profile cases became the victims of inaccurate
    information sharing between Canada and the US resulting in the torture of
    these Canadian citizens. The Canadian government subsequently conducted a
    report and apologized for these. Nonetheless, the antiterrorism laws,
    increased border controls and deeply problematic representations of Muslims
    as terrorists after 9/11 has continued to impact all groups of Canadian
    Muslims.
  </div>

