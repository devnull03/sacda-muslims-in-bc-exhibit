      <h2>Muslim Identity as National and Transnational</h2>
      <div>
        Muslims of South Asian descent came to British Columbia from many
        different places, mainly because their pasts were connected to Empire
        and Empire was global. In other words, South Asian Muslims were part of
        a layered migration history which means that their identity is both
        national (connected to countries like India, Pakistan, Bangladesh, Sri
        Lanka, Nepal and Bhutan) as well as Transnational (connected to multiple
        waves of migration that connect them to South Asia, but also connect
        their ancestral story to places in the Carribbean and Africa). Some
        scholars use the term “double diaspora” to refer to communities that
        have experienced multiple migration stages, leading to identities that
        merge influences from different regions.
        <br /><br />
        Many South Asian Muslim in British Columbia trace their roots to Punjab,
        Gujarat, Fiji or other parts of South Asia, but their migration to Canada
        did not happen in a linear fashion. For instance, Gujarati Muslims who had
        a long history of being involved in trading networks were a global community.
        Scholars have pointed out how Muslim traders across the Indian Ocean developed
        different ideas of a transnational identity which became more important than
        their country of birth. Many of these links were subsequently disrupted by
        the emergence of European colonialism in the Indian Ocean. In the interviews
        conducted for this project, one of the respondents pointed toward their family
        sharing such an identity rooted in their business and trade links. See X
        <br /><br />

        Simultaneously, other groups of South Asian Muslims were sent with other
        South Asian workers to British colonies in East Africa like Uganda to
        work on the railways that the British Empire was building. Those who
        went to Uganda would be displaced again in 1972 when Idi Amin expelled
        Asians from the country (see 1972 in Historical Timeline). The rise of
        the use of Asians as indentured workers in colonies closely followed the
        end of abolition of slavery and so these workers were, in some cases,
        replacing African slave labour. The experiences of Fijian Muslims was
        similar, as they arrived in the Pacific as indentured laborers under
        British colonial rule and went on to form tightly knit religion and
        cultural communities before a second wave of migration Canda took place
        in the later 20th century.
        <br /><br />

        These “double diaspora” Muslims came to British Columbia carrying a
        combination of religious, social and cultural traditions that meshed the
        multiple journeys that were part of their life history. So their Islamic
        identity was not just South Asian, but influenced by East African or
        Fijian experiences resulting in unique local expressions of Islam in
        Canada.
        <br /><br />

        Once they arrived in British Columbia, many of these Muslims became part
        of various organizations and associations. Some of these were regionally
        specific while others were organized around a particular Islamic sect.
        These organizations began to appear in BC from the 1960s onward and,
        alongside local mosques provide religious education, marriage services
        and so on.
        <br /><br />

        Muslim identity continues to be both deeply rooted in their local
        practices and yet connected to international events and organizations.
        For instance, conflicts in Palestine, Kashmir or Afghanistan that
        directly influence Muslims deeply impact the local Muslim community
        which organizes to support the global Muslim ummah (or the global
        Islamic community). Religious networks and scholarly exchanges also take
        place. Some of these are more formal, for instance through orgnizations
        like the cosmopolitan Ismaili community while others are more informal
        for instance how young people are exposed to global Islamic discourse
        through their consumption of online information and media.
        <br /><br />

        Global cultural festivals amongst the Muslim community such as Eid
        al-Fitr and Eid al-Azha are celebrated in British Columbia in ways that
        blend South Asian, African and Canadian traditions.
        <br /><br />

        As a result, the South Asian Muslim experience in British Columbia is
        both national and transnational. Rather than a monolithic “Canadian
        Islam”, what emerges is a pluralistic Muslim identity that negotiates
        between regional traditions, religious doctrines, and what it means to
        be Canadian.
