      <h2>Bengali</h2>
      <div>
        Due to a lack of records about early Bengali migration in B.C, Taraknath
        Das (1884-1958) is one of the few Bengalis who is known to have lived in
        B.C during the early twentieth century. Another well known Bengali who
        set foot in B.C during this time period was the great poet, Rabindaranth
        Tagore (1861-1941), whose visit to Vancouver in April 1929 remains a
        cherished memory for the Bengali community of B.C.
        <br /><br />
        Beyond this early twentieth century time period, it was mostly in 1970's
        that a number of Bengali community members started migrating to Canada. Consequently,
        Bengali organizations started emerging across Canada during this time period.
        In the case of B.C, Lower Mainland Bengali Cultural Society of BC (LMBCS)
        was formed in 1977. Most of its executive members came from a highly educated,
        urban professional class, called <i>bhadralok</i>.<sup class="c6"
          ><a href="#ftnt16" id="ftnt_ref16">[16]</a></sup
        >
        Alongside, Canada Bangladesh Culture Association of B.C was established around
        1988-89, and the Bangladesh Cultural was established in 1992-93.<sup
          class="c6"><a href="#ftnt17" id="ftnt_ref17">[17]</a></sup
        >
        While these Bengali organizations were not overtly Islamic, a number of their
        community participants were muslims.
        <br /><br />
        In 1998, a Bengali community member in Vancouver, Rafiqul Islam (1950-2013),
        established Mother Language Lovers of the World Society, with his fellow
        colleague Abdus Salam. The Society played a significant role in getting 21st
        February declared as International Mother Language Day in 1999.
      </div>
    </section>

    <section>
      <h2>The Caribbean</h2>
      <div>
        From 1838 to 1917, the British colonial government in India sent over
        “500,000” East Indians to the Caribbean countries like Guyana, Trinidad,
        Tobago and Suriname as indentured laborers.<sup class="c6"
          ><a href="#ftnt18" id="ftnt_ref18">[18]</a></sup
        >
        Around “16%” of these indentured laborers were Muslims, most of them belonging
        to North India.<sup class="c6"
          ><a href="#ftnt19" id="ftnt_ref19">[19]</a></sup
        >
        Some of them returned back to India once their indenture contract ended,
        while more than two third East Indians decided to stay in the Caribbean.<sup
          class="c6"><a href="#ftnt20" id="ftnt_ref20">[20]</a></sup
        >
        “Unemployment, poverty, illiteracy, and suicide” was high in the East Indian-Carribean
        communities of late nineteenth century, but they also tried to “restore,
        recreate, and reconsolidate some aspects of their homeland culture.”<sup
          class="c6"><a href="#ftnt21" id="ftnt_ref21">[21]</a></sup
        > After the independence of Caribbean countries in the 1960s, a number of
        these Indo-Caribbean community members including muslims started migrating
        to Canada and B.C.
      </div>
    </section>

    <section>
      <h2>Fiji</h2>
      <div>
        Fiji Muslims constitute another important community of Muslims in B.C
        who trace back their ancestry to South Asia.
        <br /><br />
        Along with the Indo Caribbean Islands, Fiji was another location where East
        Indians were sent as indentured laborers between 1879 and 1920. Within these
        41 years, over “60,553” East Indians were transported to Fijian shores.<sup
          class="c6"><a href="#ftnt22" id="ftnt_ref22">[22]</a></sup
        >
        Around “7,200” of them were muslims.<sup class="c6"
          ><a href="#ftnt23" id="ftnt_ref23">[23]</a></sup
        >
        Because of the miserable living and working conditions, “more suicides occurred
        in Fiji than any other British Colony at the time.”<sup class="c6 c28"
          ><a href="#ftnt24" id="ftnt_ref24">[24]</a></sup
        >
        Over time, Indo-Fijian communities also adapted their religious and cultural
        practices to the new Fijian environment they found themselves in.
        <br /><br />
        After the abolition of indentured labor in 1920, around 60% of Indo Fijians
        decided to stay back in Fiji.<sup class="c6"
          ><a href="#ftnt25" id="ftnt_ref25">[25]</a></sup
        >
        A series of military coups d'état in Fiji between 1987 and 2000 resulted
        in an increased discrimination against Indo Fijian communities, forcing them
        to migrate to countries like Canada.<sup class="c6"
          ><a href="#ftnt26" id="ftnt_ref26">[26]</a></sup
        >
      </div>
    </section>

    <section>
      <h2>East Africa / Ismaili Muslims</h2>
      <div>
        A majority of Ismaili community migrated from the East African countries
        of Uganda, Kenya, and Tanzania to Canada. Most of the community members
        trace their origins back to India/Gujarat. Their traditional practices
        draw from the particular Khoja Ismaili heritage of Western India.<sup
          class="c6"><a href="#ftnt27" id="ftnt_ref27">[27]</a></sup
        >
        <br /><br />
        The migration of Ismaili Muslims from Tanzania and Uganda to Canada in the
        1970s was a result of political instability, discriminatory policies, and
        the quest for a better and secure future. While there was relatively low,
        but consistent migration coming in from the Ismaili population in Tanzania,
        there was a sudden abundance coming in after the implementation of the nationalization
        policies outlined in the Arusha Declaration of 1967. Tanzanians were some
        of the first Ismailis to immigrate to Canada, and hence, were instrumental
        in the founding of the country's first Jamatkhanas/ Prayer Halls. A Jamatkhana
        built in Burnaby in 1985 was the first purpose-built Ismaili jamatkhana and
        the first Ismaili centre in North America. It cost over $10 million.
        <br /><br />
        Alongside, a large number of Ugandan-Asian Ismaili Muslims also started migrating
        to Canada after they were ordered to leave Uganda within 90 days by Idi Amin
        in August 1972. A particularly large community of these East African Ismaili
        Muslims settled in Vancouver and its surrounding areas. During the early
        1970s, the Citizen newspaper of Prince George, welcomed the refugees coming
        in from Uganda.
        <br /><br />

