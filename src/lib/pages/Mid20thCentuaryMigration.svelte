      <h2>Community Activism</h2>
      <div>
        Despite the Canadian government's promotion of multicultural values
        since 1971, racism against South Asian communities including muslims was
        on the rise in B.C during this time period. They would openly be
        harassed, and beaten on the streets, and their homes would be
        vandalized. Similarly, they would often be underpaid and forced to work
        under miserable conditions. The plans to build mosques in Richmond,
        Edmonton, and Port Coquitlam also initially faced opposition from the
        public and municipal authorities. Lastly, the local newspaper reportings
        would also often pander hatred against South Asian communities and
        muslims.
        <br /><br />
        In the light of these events, South Asian muslim community members often
        joined diverse organizations like Canadian Farmworkers Union (1979-2004),
        Indian People's Association of North America (1973-1995), BC Organization
        to Fight Racism (1973-1990), and BC Muslim Association to resist and fight
        back.
        <br /><br />
        Outside Canada, the events in India during this time period also affected
        the muslim communities of B.C. A conference organized in Vancouver in 1987,
        titled “Centralized State Power and the threat to minorities in India” involved
        active participation of Council of Muslim Communities of Canada, B.C Muslim
        Association, and Canadian Society of Fiji Muslims. Similarly, following the
        attack on Babri Mosque in India in December 1992, Dr. Hari Sharma, a well
        known activist and professor at SFU, formed Non-Resident Indians for Secularism
        and Democracy (NRISAD). One of the aims of NRISAD was to unite South Asian
        people of diverse religions, cultures, histories, and geographies, including
        Muslims, together.<sup class="c6"
          ><a href="#ftnt28" id="ftnt_ref28">[28]</a></sup
        >
        The 50th anniversary of South Asia's independence organized by NRISAD in
        1997 also involved a number of Muslim community organizations.
