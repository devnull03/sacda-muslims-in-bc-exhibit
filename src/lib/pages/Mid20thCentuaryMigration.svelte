      <h2>Muslim women</h2>
      <div>
        India Mahila Association was formed in 1973 by a group of Indian women
        to highlight and tackle the issue of domestic violence in South Asian
        communities of B.C. According to one of its press releases, the
        Association consisted of women from Pakistan, Bangladesh, East Africa,
        Fiji, Europe, and India, who were all united in their fight against
        domestic violence.
        <br /><br />
        In 1988, BCMA also established its women's chapter to cater to the needs
        and demands of muslim women.<sup class="c6"
