    <section>
      <h2>Muslim Queerness</h2>
      <div>
        Itrath Syed, a former BCMA member, faced backlash from community members
        for her support of the same sex marriage during her election campaigns
        in 2004.<sup class="c6"><a href="#ftnt30" id="ftnt_ref30">[30]</a></sup>
        More recently, a number of community members participated in “Million March
        4 Children” to protest against sexual orientation and Gender identity (SOGI)
        curriculum in schools.<sup class="c6"
          ><a href="#ftnt31" id="ftnt_ref31">[31]</a></sup
        >
        These incidents indicate the precarity of queer South Asian muslims within
        the community.
        <br /><br />
        With the legalization of same-sex marriage in 2005, organizations like Trikone
        Vancouver, Sher Vancouver, and Salaam Queer Muslim community emerged to offer
        support to queer South Asian communities in B.C. El-Farouk Khaki, Imtiaz
        Popat, and Fatima Jaffer are the three well known South Asian muslim queer
        activists.
