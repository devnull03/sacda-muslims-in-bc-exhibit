    <section>
      <h2>Bengali</h2>
      <div>
        Recently, Bengali organizations in B.C have started regularly organizing
        events like West Coast Tagore Festival, International Mother language
        Festival, Pitha Utsav (festival of Bangladeshi pancakes, desserts), and
        Vaisakhi Mela (Bengali New Year celebration). These events are well-
        attended by Bengali community members of various religions, and
        nationalities, including Muslims.<sup class="c6"
          ><a href="#ftnt32" id="ftnt_ref32">[32]</a></sup
        >
      </div>
    </section>

    <section>
      <h2>Sri Lanka</h2>
      <div>
        Sri Lanka's diverse Muslim community counts up to 9.7% of the total
        population. While the majority of the Muslims are Sunni, there are
        communities of those who follow Sufism. The beginnings of the Muslim
        population in Sri Lanka can be linked to the ancient trade paths
        connecting South and Southeast Asia with the Middle East. In the seventh
        century, traders from the Middle East (Arabs and Persians) established
        commercial connections in southern India that also extended to Sri
        Lanka. Middle Eastern traders wed Tamil and Sinhalese women and
        established roots in the eastern region of the island near Batticaloa
        and Ampara. Tuan Burhanuddin Jayah is seen as one of the national heroes
        of Sri Lanka and was the first Muslim cabinet minister in 1948.<sup
          class="c6"><a href="#ftnt33" id="ftnt_ref33">[33]</a></sup
        >
      </div>
    </section>

    <section>
      <h2>Ahmadiyya</h2>
      <div>
        Most of the Ahmadiyya community members in B.C consist of refugees and
        immigrants from Pakistan's Punjab region. A large number of community
        members have migrated to Canada to escape religious persecution
        especially after 1974 when they were declared non-muslims in Pakistan.
        Baitur Rehman Mosque was built in 2005 to fulfill the religious needs of
        the community. The community has also actively developed relationships
        with Indigenous communities of B.C.<sup class="c6"
          ><a href="#ftnt34" id="ftnt_ref34">[34]</a></sup
        >
        Lastly, the community also organizes the annual World Religions Conference
        across B.C.<sup class="c6"
          ><a href="#ftnt35" id="ftnt_ref35">[35]</a></sup
        >
      </div>
    </section>

    <section>
      <h2>Shia</h2>
      <div>
        As the recent work of Waged Jafer indicates, the historical narrative of
        Imam Hussain guides the self understanding of the Shia community in
        Vancouver. Amidst a predominantly Sunni Muslim presence in Canada, Shia
        community members often find themselves as a minority within a minority.
        Within this diasporic context, they actively “resist marginalization,
        seek equal recognition and equal participation” by mourning and
        remembering the story of Imam Hussain.<sup class="c6"
          ><a href="#ftnt36" id="ftnt_ref36">[36]</a></sup
        >
        <br /><br />
        Shia Muslim Community of British Columbia (SMCBC) is the largest organization
        of the community in B.C.<sup class="c6"
          ><a href="#ftnt37" id="ftnt_ref37">[37]</a></sup
        > In 2002, the organization opened Az-Zahra Islamic Centre, which caters
        to the “spiritual, social, educational, and economic well being” of the community.
      </div>
    </section>

